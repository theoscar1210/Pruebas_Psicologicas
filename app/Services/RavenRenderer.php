/**
 * Renderer SVG server-side para las Matrices Progresivas de Raven (MPR-SL).
 * Porta la lógica del motor gráfico JS original a PHP puro.
 * No requiere archivos de imagen externos.
 */
class RavenRenderer
{
    private const FILLS = ['#fff', '#ccc', '#888', '#1a1a1a'];
    private const SK    = '#1a1a1a';
    private const SW    = 2.2;

    // ─── Descriptor de forma ────────────────────────────────────────────────
    // t=tipo, f=fill(0-3), s=tamaño, x=posX, y=posY, r=rotación
    private static function s(
        string $t, int $f = 3, int $s = 26,
        int $x = 50, int $y = 50, int $r = 0
    ): array {
        return compact('t', 'f', 's', 'x', 'y', 'r');
    }

    // ─── Renderiza una forma a la escala K ─────────────────────────────────
    public static function shp(array $sh, float $K): string
    {
        $t    = $sh['t'];
        $f    = (int)($sh['f'] ?? 3);
        $sz   = ($sh['s'] ?? 26) * $K;
        $cx   = ($sh['x'] ?? 50) * $K;
        $cy   = ($sh['y'] ?? 50) * $K;
        $r    = $sh['r'] ?? 0;
        $fill = self::FILLS[min($f, 3)];
        $sk   = self::SK;
        $sw   = self::SW;
        $tr   = $r ? sprintf(' transform="rotate(%s,%s,%s)"', $r, $cx, $cy) : '';
        $n    = fn(float $v): string => rtrim(rtrim(sprintf('%.4f', $v), '0'), '.');

        switch ($t) {
            case 'C':
                return sprintf('<circle cx="%s" cy="%s" r="%s" fill="%s" stroke="%s" stroke-width="%s"/>',
                    $n($cx), $n($cy), $n($sz), $fill, $sk, $sw);

            case 'S':
                return sprintf('<rect x="%s" y="%s" width="%s" height="%s" fill="%s" stroke="%s" stroke-width="%s"%s/>',
                    $n($cx-$sz), $n($cy-$sz), $n($sz*2), $n($sz*2), $fill, $sk, $sw, $tr);

            case 'T':
                $p = implode(' ', [
                    $n($cx).','.  $n($cy-$sz),
                    $n($cx+$sz*.866).','.  $n($cy+$sz*.5),
                    $n($cx-$sz*.866).','.  $n($cy+$sz*.5),
                ]);
                return sprintf('<polygon points="%s" fill="%s" stroke="%s" stroke-width="%s"%s/>',
                    $p, $fill, $sk, $sw, $tr);

            case 'D':
                $p = implode(' ', [
                    $n($cx).','.  $n($cy-$sz),
                    $n($cx+$sz*.68).','.  $n($cy),
                    $n($cx).','.  $n($cy+$sz),
                    $n($cx-$sz*.68).','.  $n($cy),
                ]);
                return sprintf('<polygon points="%s" fill="%s" stroke="%s" stroke-width="%s"%s/>',
                    $p, $fill, $sk, $sw, $tr);

            case 'P':
                $pts = [];
                for ($i = 0; $i < 5; $i++) {
                    $a     = ($i * 72 - 90) * M_PI / 180;
                    $pts[] = $n($cx + $sz*cos($a)).','.  $n($cy + $sz*sin($a));
                }
                return sprintf('<polygon points="%s" fill="%s" stroke="%s" stroke-width="%s"%s/>',
                    implode(' ', $pts), $fill, $sk, $sw, $tr);

            case 'H':
                $pts = [];
                for ($i = 0; $i < 6; $i++) {
                    $a     = ($i * 60 - 30) * M_PI / 180;
                    $pts[] = $n($cx + $sz*cos($a)).','.  $n($cy + $sz*sin($a));
                }
                return sprintf('<polygon points="%s" fill="%s" stroke="%s" stroke-width="%s"%s/>',
                    implode(' ', $pts), $fill, $sk, $sw, $tr);

            case 'X':
                $w    = $sz * .32;
                $sw04 = $sw * .4;
                return sprintf('<g%s><rect x="%s" y="%s" width="%s" height="%s" fill="%s" stroke="%s" stroke-width="%s"/>'
                             . '<rect x="%s" y="%s" width="%s" height="%s" fill="%s" stroke="%s" stroke-width="%s"/></g>',
                    $tr,
                    $n($cx-$w),  $n($cy-$sz), $n($w*2),  $n($sz*2), $fill, $sk, $sw04,
                    $n($cx-$sz), $n($cy-$w),  $n($sz*2), $n($w*2),  $fill, $sk, $sw04);

            case 'A':
                $bw = $sz*.35; $hw = $sz*.65;
                $bl = $sz*1.1; $h  = $sz*1.7/2;
                $p  = implode(' ', [
                    $n($cx-$h).','.     $n($cy-$bw),
                    $n($cx+$bl-$h).','. $n($cy-$bw),
                    $n($cx+$bl-$h).','. $n($cy-$hw),
                    $n($cx+$h).','.     $n($cy),
                    $n($cx+$bl-$h).','. $n($cy+$hw),
                    $n($cx+$bl-$h).','. $n($cy+$bw),
                    $n($cx-$h).','.     $n($cy+$bw),
                ]);
                return sprintf('<polygon points="%s" fill="%s" stroke="%s" stroke-width="%s"%s/>',
                    $p, $fill, $sk, $sw, $tr);

            case 'L':
                $lc   = $f === 0 ? '#fff' : ($f === 1 ? '#bbb' : ($f === 2 ? '#777' : '#1a1a1a'));
                $sw18 = $sw * 1.8;
                return sprintf('<line x1="%s" y1="%s" x2="%s" y2="%s" stroke="%s" stroke-width="%s" stroke-linecap="round"%s/>',
                    $n($cx), $n($cy-$sz), $n($cx), $n($cy+$sz), $lc, $sw18, $tr);

            default:
                return '';
        }
    }

    // ─── Envuelve un array de formas en un SVG ──────────────────────────────
    public static function cellSVG(array $shapes, int $px): string
    {
        $K     = $px / 100;
        $inner = '';
        foreach ($shapes as $sh) {
            $inner .= self::shp($sh, $K);
        }
        return sprintf('<svg width="%d" height="%d" viewBox="0 0 %d %d" style="display:block">%s</svg>',
            $px, $px, $px, $px, $inner);
    }

    // ─── Renderiza la cuadrícula 3×3 de la matriz ───────────────────────────
    public static function matrix(array $item, int $cellPx = 90): string
    {
        $html = '';
        foreach ($item['matrix'] as $row) {
            foreach ($row as $cell) {
                if ($cell === null) {
                    $html .= '<div class="raven-cell raven-empty"><div class="raven-q">?</div></div>';
                } else {
                    $html .= '<div class="raven-cell">' . self::cellSVG($cell, $cellPx) . '</div>';
                }
            }
        }
        return $html;
    }

